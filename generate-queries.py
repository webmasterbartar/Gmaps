def generate_queries(output_file="queries.txt"):
    """Generate all query combinations"""
    
    total_queries = len(furniture_queries) * len(iranian_provinces)
    
    print(f"Generating {total_queries} queries...")
    print(f"  {len(furniture_queries)} furniture types")
    print(f"  × {len(iranian_provinces)} provinces")
    print(f"  = {total_queries} total queries\n")
    
    with open(output_file, 'w', encoding='utf-8') as f:
        # f.write("# Google Maps Queries - فروشگاه‌های مبلمان در استان‌های ایران\n")
        # f.write(f"# تعداد کل: {total_queries} query\n")
        # f.write(f"# {len(furniture_queries)} نوع کوئری × {len(iranian_provinces)} استان\n\n")
        
        for query in furniture_queries:
            # f.write(f"# {query}\n")
            for province in iranian_provinces:
                f.write(f"{query} در {province}\n")
            # f.write("\n")
    
    print(f"✓ Successfully generated {total_queries} queries")
    print(f"✓ Saved to: {output_file}")

